package collectionDemo;

public class Array {

	public static void main(String[] args) {
		
		int a[] = new int[3]; // declaration
		a[0] = 10;  // size 1 int i =10;  initialisation
		a[1] = 230;	// size 2 int j = 20;
		a[2] = 15;	// size 3 int k = 15;
		// how to travel array
		for(int i = 0; i <=2; i ++)
		{
			System.out.println(a[i]);  //a[1]
		}
		
		
	}

}
