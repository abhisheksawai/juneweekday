package collectionDemo;

public class TypeCasting {

	public static void main(String[] args) {
		
		int x = 'a';
		System.out.println(x);
		
		double d = 'B';
		System.out.println("--s--");
		System.out.println(d);
		
		double dd = 'a';
		System.out.println("------");
		System.out.println(dd);
		
		int x1 = 130;
		byte b = (byte) x1; 
		System.out.println(b);
		

	}

}
